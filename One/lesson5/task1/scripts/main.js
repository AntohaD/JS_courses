"use strict"

// 1. Создать функцию double(), которая будет принимать в качестве 
// аргумента число и удваивать его. 
// Удвоенное число либо пусть функция возвращает через return, 
// либо сразу выводит через console.log. Например
// double(5) выдаст 10.

function double(a) {
    return a * 2;
}

console.log(double(5));